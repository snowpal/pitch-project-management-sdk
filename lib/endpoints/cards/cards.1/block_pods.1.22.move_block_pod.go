package cards

import (
	"fmt"
	"net/http"

	"github.com/snowpal/pitch-building-projects-sdk/lib"
	"github.com/snowpal/pitch-building-projects-sdk/lib/helpers"
	"github.com/snowpal/pitch-building-projects-sdk/lib/structs/request"
)

func MoveCard(jwtToken string, podParam request.CopyMoveProjectCardParam) error {
	route, err := helpers.GetRoute(
		lib.RouteCardsMoveCard,
		podParam.CardId,
		podParam.KeyId,
		podParam.ProjectId,
		podParam.TargetKeyId,
		podParam.TargetProjectId,
	)
	if err != nil {
		fmt.Println(err)
		return err
	}

	var req *http.Request
	req, err = http.NewRequest(http.MethodPatch, route, nil)
	if err != nil {
		fmt.Println(err)
		return err
	}

	helpers.AddUserHeaders(jwtToken, req)

	_, err = helpers.MakeRequest(req)
	if err != nil {
		fmt.Println(err)
		return err
	}
	return nil
}
